    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed

        // La búsqueda se hace por email
        
        Usuario1 user= new Usuario1();
        boolean encontrado=false;
        String email;
        email=textContraseña.getText();
        
        //Comprobar que la lista no está vacía con isEmpty
        if(!usuarios.isEmpty()){
            for(int i=0; i<usuarios.size()&&!encontrado;i++){
               // profe=profesores.get(i);
                if(email.equalsIgnoreCase(user.getEmail())){
                    encontrado=true;
                }
            }
        if(encontrado){
            textContraseña.setText(user.getContraseña());
            textUsuario.setText(user.getEmail());
            textDescuento.setText(user.getDescuento()+"");
            if(user.isPremium()){
                opcionSiPremium.setSelected(true);
            }else{
                opcionNoPremium.setSelected(true);
            }
            }else{
                JOptionPane.showMessageDialog(this, "NO existen coincidencias.", "No encontrado", JOptionPane.INFORMATION_MESSAGE);    
            }
            }else{
            JOptionPane.showMessageDialog(this,"No hay Usuarios dados de alta." , "Lista vacía", JOptionPane.WARNING_MESSAGE);
            }
    }//GEN-LAST:event_botonBuscarActionPerformed

    private void botoningresosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botoningresosActionPerformed
        // TODO add your handling code here:
         Double tarifa=0.0;
         Double prem=35.5; //tarifa premium
         Double noprem=20.5; //tarifa normal
         Double desc=(Double.parseDouble(textDescuento.getText()));
         if (opcionSiPremium.isSelected()){
           tarifa=prem-desc;
           JOptionPane.showMessageDialog(this,"La tarifa para este usuario es: "+prem+"-"+desc+"="+tarifa , "Ingresos", JOptionPane.WARNING_MESSAGE);
         }else {
           tarifa=noprem-desc;
          JOptionPane.showMessageDialog(this,"La tarifa para este usuario es: "+noprem+"-"+desc+"="+tarifa , "Ingresos", JOptionPane.WARNING_MESSAGE);
       }
        
    }//GEN-LAST:event_botoningresosActionPerformed

