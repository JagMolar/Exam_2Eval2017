    private void botonAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAñadirActionPerformed
        
        // Creamos objeto user para guardar los datos
        Usuario1 User =new Usuario1();
        
        //Controlamos si el email es correcto 
        boolean correcto=true;
        
        Pattern ident = Pattern.compile("^([\\w-]+\\.)*?[\\w-]+@[\\w-]+\\.([\\w-]+\\.)*?[\\w]+$");
        Matcher id=ident.matcher(textUsuario.getText());
        if(id.matches())
             User.setEmail(textUsuario.getText());
         else{
             JOptionPane.showMessageDialog(this,"Formato de usuario incorrecto, debe ser un email válido." , "Error", JOptionPane.ERROR_MESSAGE);
             correcto=false;
             }
             User.setContraseña(textContraseña.getText());
             try{
                User.setDescuento(Double.parseDouble(textDescuento.getText()));
                }catch(NumberFormatException e) {
                JOptionPane.showMessageDialog(this,"Error al introducir el importe del descuento" , "Error", JOptionPane.ERROR_MESSAGE);
                correcto=false;
                }
                if (opcionSiPremium.isSelected()){
                    User.setPremium(true);
                }else
                    User.setPremium(false);
                if (correcto){
                    JOptionPane.showMessageDialog(this,"Usuario añadido correctamente." , "Añadido", JOptionPane.INFORMATION_MESSAGE);
                    usuarios.add(User);
                }else{
                    JOptionPane.showMessageDialog(this,"No se añadió el usuario." , "Error", JOptionPane.ERROR_MESSAGE);
                    }
            
    }//GEN-LAST:event_botonAñadirActionPerformed


